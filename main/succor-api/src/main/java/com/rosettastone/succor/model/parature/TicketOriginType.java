package com.rosettastone.succor.model.parature;

/**
 * This class represents values for origin field in Outreach ticket.
 */
public enum TicketOriginType implements Identifiable {

    PHONE("parature.field.ticketOrigin.phone"),
    SUPER("parature.field.ticketOrigin.superTicket");

    private final String key;

    private TicketOriginType(String key) {
        this.key = key;
    }

    public String getKey() {
        return key;
    }

}

