public class ActionCsr {

    private String id;
    private Long assignToCsr;
    private String comment;
    private boolean showToCust;
    private int timeSpentMinutes;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Long getAssignToCsr() {
        return assignToCsr;
    }

    public void setAssignToCsr(Long assignToCsr) {
        this.assignToCsr = assignToCsr;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public boolean isShowToCust() {
        return showToCust;
    }

    public void setShowToCust(boolean showToCust) {
        this.showToCust = showToCust;
    }

    public int getTimeSpentMinutes() {
        return timeSpentMinutes;
    }

    public void setTimeSpentMinutes(int timeSpentMinutes) {
        this.timeSpentMinutes = timeSpentMinutes;
    }

    @Override
    public String toString() {
        return "ActionCsr{" +
                "id='" + id + '\'' +
                ", assignToCsr=" + assignToCsr +
                ", comment='" + comment + '\'' +
                ", showToCust=" + showToCust +
                ", timeSpentMinutes=" + timeSpentMinutes +
                '}';
    }
}
