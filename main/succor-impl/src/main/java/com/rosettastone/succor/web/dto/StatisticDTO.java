package com.rosettastone.succor.web.dto;


import java.util.List;

/**
 * The {@code StatisticDTO} represents statistic for flex.
 */
public class StatisticDTO {

    public List<CounterDTO> counters;
    public List<TaskErrorDTO> errors;

    public List<CounterDTO> getCounters() {
        return counters;
    }

    public void setCounters(List<CounterDTO> counters) {
        this.counters = counters;
    }

    public List<TaskErrorDTO> getErrors() {
        return errors;
    }

    public void setErrors(List<TaskErrorDTO> errors) {
        this.errors = errors;
    }
}
